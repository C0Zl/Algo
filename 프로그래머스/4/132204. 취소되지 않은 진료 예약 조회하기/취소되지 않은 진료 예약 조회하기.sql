SELECT APNT_NO, PT_NAME, P.PT_NO, A.MCDP_CD, DR_NAME, APNT_YMD
FROM APPOINTMENT A
JOIN PATIENT P ON A.PT_NO = P.PT_NO
JOIN DOCTOR D ON D.DR_ID = A.MDDR_ID
WHERE APNT_YMD >= TO_DATE('2022-04-13', 'YYYY-MM-DD')
AND APNT_YMD < TO_DATE('2022-04-14', 'YYYY-MM-DD')
AND APNT_CNCL_YN = 'N'
AND A.MCDP_CD = 'CS'
ORDER BY APNT_YMD